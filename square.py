
def area(a):
    '''
    Вовращает площадь квадрата.

        Параметры:
            a (int): десятичное целое число.

        Возвращаемое значение:
            a * a (int): десятичное целое число.

    '''
    return a * a


def perimeter(a):
    '''
    Вовращает периметр квадрата.

        Параметры:
            a (int): десятичное целое число.

        Возвращаемое значение:
            4 * a (int): десятичное целое число.

    '''
    return 4 * a
